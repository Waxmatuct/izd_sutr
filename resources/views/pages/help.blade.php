                <div>
                    <h2 id="" class="text-2xl sm:text-3xl font-bold mt-10">Аннотация</h2>
                </div>

                <p>Аннотация — это краткая, точная, логические связанная и грамотно изложенная информация о содержании книги
                    (10—15 строк). Она не должна содержать второстепенной информации.</p>
                <p>Основная функция аннотации — сигнальная, позволяющая читателю установить основное содержание книги и
                    решить, необходимо ли обращаться к первоисточнику.</p>
                <p>По своему назначению аннотация — справочная, т. к. уточняет неясное заглавие и сообщает в справочных
                    целях сведения об авторе, содержании и других особенностях документа.</p>
                <p>По объему и глубине свертывания информации — описательная: обобщенно характеризует содержание и приводит
                    перечень основных тем, отвечает на вопрос «О чем сообщается в пособии?».</p>
                <p>Аннотацию желательно строить из коротких фраз, не употреблять разновременные глаголы, например:
                    «Представлены» и «Представляются», т. е. соблюдать единство времени во всех предложения.</p>
                <p>Язык аннотации должен быть прост и доходчив. Следует избегать лишних вводных фраз. Рекомендуется
                    употреблять синтаксические конструкции, свойственные языку научных дисциплин, избегать сложных
                    предложений, включающих несколько придаточных.</p>
                <p>Фразы следует строить комплексно, употреблять стандартизированную терминологию, не использовать
                    малораспространенные термины или разъяснять их при первом упоминании в тексте, соблюдать единство
                    терминологии.</p>
                <p>Еще раз напомним, что аннотация включает характеристику темы, про-блемы, цели работы и ее основные
                    выводы.</p>

