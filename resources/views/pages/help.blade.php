                <p>Таблицу рекомендуется размещать после ссылки на неё в тексте, обязательно в пределах данного раздела, т.
                    е. до следующего заголовка, но не непосредственно перед ним. Рубрика или всё издание не может
                    заканчиваться таблицей. Таблица должна быть закрыта двумя-тремя строками текста после неё.</p>
                <p>Таблицы могут быть «закрытыми» (взятыми в рамку из линеек со всех сторон), «частично закрытыми» или
                    «открытыми» (без внешних линеек). В каждом издании следует придерживаться единообразия в оформлении
                    табличного материала.</p>
                <p>Система нумерации таблицы может быть сквозной через всё издание, сквозной постатейно, индексационной
                    (поглавной). Если таблица единственная в издании или статье, то её не нумеруют, следовательно, отпадает
                    надобность и в нумерационном заголовке: ставить в заголовке слово «Таблица» без номера нет смысла:
                    читатель и так знает, что перед ним таблица.</p>
                <p>Тематический заголовок определяет тему и содержание таблицы. Он нужен для того, чтобы читатель мог
                    пользоваться таблицей, не обращаясь к основному тексту. Тематический заголовок ставят над таблицей под
                    её нумерационным заголовком, выделяют шрифтом (обычно полужирного начертания), без знака препинания в
                    конце.</p>
                <p>Наиболее распространённая форма: слово «Таблица» и её номер арабскими цифрами (без знака номера перед
                    ними, без точки на конце) ставят над тематическим заголовком. Нумерационный заголовок вставляют в правый
                    край набора и чаще всего выделяют курсивом или разрядкой.</p>
                <p>Над продолжением таблицы на новой полосе помещают заголовок «Продолжение табл. 1» (если таблица на этой
                    полосе не оканчивается) или «Окончание табл. 1» (если таблица здесь завершается).</p>
                <p>Оставлять ячейки таблицы пустыми не допускается, при отсутствии сведений в ячейке ставят тире.</p>

